
# Вводим курс валюты
exchange_rate = float(input("Введите курс валюты: "))

# Запрашиваем у пользователя сумму для конвертации
amount = float(input("Введите сумму для конвертации: "))

# Вычисляем конвертированную сумму
converted_amount = amount * exchange_rate

# Выводим конвертированную сумму
print("Конвертированная сумма:", converted_amount)